package java_practice;

public class JavaPracticeDatatypes {

	public static void main(String[] args) {
		System.out.println("Hello world");
		String name = "subash";
		System.out.println(name);
		byte age = 26;
		System.out.println("The age is "+age);
		float weight = 65.5f;
		System.out.println("The weight is "+ weight);
		double height = 165.561614916911;
		System.out.println("The height is " + height );
		long phoneNumber = 8680835412L;
		System.out.println("The phone number is "+ phoneNumber);
		int number = 1273128322;
		System.out.println("The number is "+ number);
		short salary = 10000;
		System.out.println("The salary is "+ salary);
		char initial = 'k';
		System.out.println("The initial is " + initial);
		boolean liked = true;
		System.out.println("The boolena value is " + liked );
		
		//Primitive data type :
		//byte - 1 byte = -128 to 127 
		//short - 2 bytes = whole number -32,768 to 32,767 
		//int - 4 bytes = whole number -2,147,483,648 to 2,147,483,647
		//long - 8 bytes = whole number -9,223,372,036,854,755,808 to 9,223,372,036,854,775,807
		//float - 4 bytes = 6 to 7 decimal digits 
		//double - 8 bytes = 15 decimal digits 
		//char - 2 bytes = stores single char 
		//boolean - 1 bit = stores true or false .
		
		//Non-Primitive data type:
		//String
		//Array
		//Classes
		//interfaces
		
		
		
		

	}

}
